u8 gFrameCounter;
struct GameModeInfo gGameMode;

static void LoadGraphics(void)
{
    WaitForVblank();

    Write8(REG_LCDC, 0);

    u8* addr;
    u16 i;

    // Tile data address
    addr = (u8*)(VRAM_BASE + 0x1000);
    for (i = 0; i < ARRAY_SIZE(sTiles); i++)
        *addr++ = sTiles[i];

    // Load tile map
    addr = (u8*)(VRAM_BASE + 0x1800);
    for (i = 0; i < ARRAY_SIZE(sTilemap); i++)
        *addr++ = sTilemap[i];

    Write8(REG_BGP, 0b11100100);
}

static void InitGame(void)
{
    gGameMode.main = GM_IN_GAME;

    // Enable v-blank intterupt
    Write8(REG_IE, INTR_VBLANK);

    LoadGraphics();

    // Enable display and background
    Write8(REG_LCDC, LCDC_LCD_ENABLE | LCDC_BG_ENABLE);
}

void main(void)
{
    InitGame();

    for (;;)
    {
        gFrameCounter++;

        // Poll inputs immediatly
        UpdateInput();

        // Do stuff...

        // Done doing stuff, wait for v-blank
        WaitForVblank();
    }
}
